export default function HowPollsWork() {
  const [open, setOpen] = useState<string | undefined>(() => {
    try {
      if (!localStorage.getItem(SEEN_KEY)) {
        localStorage.setItem(SEEN_KEY, '1');
        return 'item-0';
      }
    } catch { /* private mode */ }
    return undefined;
  });

  return (
    <Card className="mb-6">
      <CardHeader className="pb-1">
        <CardTitle className="text-base flex items-center gap-2">
          <HelpCircle className="w-4 h-4" /> Πώς λειτουργεί;
        </CardTitle>
      </CardHeader>
      <CardContent>
        <Accordion type="single" collapsible value={open} onValueChange={setOpen}>
          {SECTIONS.map((s, i) => (
            <AccordionItem key={i} value={`item-${i}`}>
              <AccordionTrigger className="text-sm text-left">{s.title}</AccordionTrigger>
              <AccordionContent className="text-sm text-muted-foreground">{s.body}</AccordionContent>
            </AccordionItem>
          ))}
        </Accordion>
      </CardContent>
    </Card>
  );
}
